class Tokenizer:
    def __init__(self, encoding_name="p50k_base"):
        self.encoding = tiktoken.get_encoding(encoding_name)
        self.bos_token = "<|endoftext|>"
        self.eos_token = "<|endoftext|>"
        self.bos_id = self.encoding.encode(self.bos_token, allowed_special={self.bos_token})[0]
        self.eos_id = self.encoding.encode(self.eos_token, allowed_special={self.eos_token})[0]
        self.n_words = self.encoding.n_vocab

    def encode(self, text, bos=False, eos=False):
        if isinstance(text, str):
            ids = self.encoding.encode(text, allowed_special={self.bos_token, self.eos_token})
        else:
            ids = [self.encoding.encode(t, allowed_special={self.bos_token, self.eos_token}) for t in text]
        
        if bos:
            ids = [self.bos_id] + ids if isinstance(ids[0], int) else [[self.bos_id] + i for i in ids]
        if eos:
            ids = ids + [self.eos_id] if isinstance(ids[0], int) else [i + [self.eos_id] for i in ids]
        
        return ids

    def decode(self, ids):
        if isinstance(ids[0], list):
            return [self.encoding.decode(id_list) for id_list in ids]
        return self.encoding.decode(ids)

